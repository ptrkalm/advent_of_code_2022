use std::str::FromStr;

pub fn part_1() -> i32 {
    let guides_rounds: Vec<&str> = INPUT.split("\n").collect();
    let guides_rounds: Vec<(Shape, Shape)> = guides_rounds.iter().map(|guides_round| {
        let guides_round: Vec<&str> = guides_round.split_whitespace().collect();
        let guides_round: (Shape, Shape) = (
            Shape::from_str(guides_round.get(0).unwrap()).unwrap(),
            Shape::from_str(guides_round.get(1).unwrap()).unwrap()
        );
        guides_round
    }).collect();

    let rounds_scores = guides_rounds.iter().map(|guides_round| {
        match guides_round {
            (Shape::Rock, Shape::Rock)          => 1 + 3,
            (Shape::Rock, Shape::Paper)         => 2 + 6,
            (Shape::Rock, Shape::Scissors)      => 3 + 0,
            (Shape::Paper, Shape::Rock)         => 1 + 0,
            (Shape::Paper, Shape::Paper)        => 2 + 3,
            (Shape::Paper, Shape::Scissors)     => 3 + 6,
            (Shape::Scissors, Shape::Rock)      => 1 + 6,
            (Shape::Scissors, Shape::Paper)     => 2 + 0,
            (Shape::Scissors, Shape::Scissors)  => 3 + 3,
        }
    }).collect::<Vec<i32>>();

    rounds_scores.iter().sum()
}

pub fn part_2() -> i32 {
    let guides_rounds: Vec<&str> = INPUT.split("\n").collect();
    let guides_rounds: Vec<(Shape, Outcome)> = guides_rounds.iter().map(|guides_round| {
        let guides_round: Vec<&str> = guides_round.split_whitespace().collect();
        let guides_round: (Shape, Outcome) = (
            Shape::from_str(guides_round.get(0).unwrap()).unwrap(),
            Outcome::from_str(guides_round.get(1).unwrap()).unwrap()
        );
        guides_round
    }).collect();

    let rounds_scores = guides_rounds.iter().map(|guides_round| {
        match guides_round {
            (Shape::Rock, Outcome::Lose)        => 3 + 0,
            (Shape::Rock, Outcome::Draw)        => 1 + 3,
            (Shape::Rock, Outcome::Win)         => 2 + 6,
            (Shape::Paper, Outcome::Lose)       => 1 + 0,
            (Shape::Paper, Outcome::Draw)       => 2 + 3,
            (Shape::Paper, Outcome::Win)        => 3 + 6,
            (Shape::Scissors, Outcome::Lose)    => 2 + 0,
            (Shape::Scissors, Outcome::Draw)    => 3 + 3,
            (Shape::Scissors, Outcome::Win)     => 1 + 6,
        }
    }).collect::<Vec<i32>>();

    rounds_scores.iter().sum()
}

#[derive(Debug)]
enum Shape {
    Rock,
    Paper,
    Scissors
}

enum Outcome {
    Lose,
    Draw,
    Win
}

impl FromStr for Shape {
    type Err = ();

    fn from_str(input: &str) -> Result<Shape, Self::Err> {
        match input {
            "A" | "X" => Ok(Shape::Rock),
            "B" | "Y" => Ok(Shape::Paper),
            "C" | "Z" => Ok(Shape::Scissors),
            _ => Err(())
        }
    }
}

impl FromStr for Outcome {
    type Err = ();

    fn from_str(input: &str) -> Result<Outcome, Self::Err> {
        match input {
            "X" => Ok(Outcome::Lose),
            "Y" => Ok(Outcome::Draw),
            "Z" => Ok(Outcome::Win),
            _ => Err(())
        }
    }
}

